/*****************************************************************************
* 函 数 名  : balong_lcd_toggle
* 功能描述  :  初始化序列解析函数
* 输入参数  :
* 输出参数  : 无
* 返 回 值  :
*****************************************************************************/
static s32 balong_lcd_toggle(struct balong_lcd_seq *seq, int sz)
{
    u8 buf[1];
    u32 ret = 0;
    int count;
    for (count = 0; count < sz; count++)
    {

        buf[0] = seq[count].value;
        if(seq[count].format == 'c')
        {
            ret = spi_write_cmd(buf, 1);  /* command */
            if(ret)
            {
                return ret;
            }

        }
        else if(seq[count].format == 'd')
        {
            ret = spi_write_data(buf, 1); /* data */
            if(ret)
            {
                return ret;
            }

        }
        else if(seq[count].format == 'w')
        {
            mdelay((seq[count].value)); /* delay */
        }
    }
    return LCD_OK;
}
/*****************************************************************************
* 函 数 名  : lcd_vectorid_get
* 功能描述  : OLED 厂家ID获取
* 输入参数  :
* 输出参数  : 无
* 返 回 值  :
*****************************************************************************/
s32 lcd_vectorid_get (u16 *pu16VectorId)
{
    u32 id0_value = 0;
    u32 id1_value = 0;
    if(!pu16VectorId)
    {
        cprintf("\r\nlcd invalid parameter.\n");
        return LCD_ERROR;
    }
    id0_value = gpio_get_value(LCD_VEC_ID_GPIO_0);
    id1_value = gpio_get_value(LCD_VEC_ID_GPIO_1);
    /* 获取 LCD 厂家ID*/
    *pu16VectorId = (BSP_U16)(((1 & id0_value) << 0) | ((1 & id1_value) << 1));

    cprintf("bootloader lcd_vectorid_get lcd ID=%x\n",*pu16VectorId);
    return LCD_OK;
}
