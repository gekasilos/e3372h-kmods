/*****************************************************************************
* 函 数 名  : oled_boot_chg_display
* 功能描述  : oled boot电池各种图标显示
* 输入参数  : 类型
* 输出参数  : 无
* 返 回 值  : 无
*****************************************************************************/
void oled_boot_chg_display(CHG_BATT_DISPLAY_TYPE disp_type) 
{
    switch(disp_type)
    {
        case CHG_DISP_OK:
            break;
        case CHG_DISP_FAIL:
            break;
        case CHG_DISP_BATTERY_LOWER:
            MainOLED_Disppic(gImage_lowbatt);
            break;
        case CHG_DISP_BATTERY_BAD:
            MainOLED_Disppic(gImage_batterror);
            break;
        case CHG_DISP_OVER_HEATED:
            MainOLED_Disppic(gImage_battovertemp);
            break;
        case CHG_DISP_TEMP_LOW:
            MainOLED_Disppic(gImage_battovertemp);
            break;
        case CHG_DISP_CHARGING:
            MainOLED_Disppic(gImage_charging);
            break;
        default:
            cprintf("oled_boot_chg_display :disp_type invalid!!!\r\n");
            break;
    }
    return;
}
